import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const root = process.cwd()
const outDir = path.join(root, 'src')
const outFile = path.join(outDir, 'buildInfo.ts')

function getTodayStamp() {
  const now = new Date()
  const y = now.getFullYear()
  const m = String(now.getMonth() + 1).padStart(2, '0')
  const d = String(now.getDate()).padStart(2, '0')
  return `${y}${m}${d}`
}

async function readPrevious() {
  try {
    const text = await readFile(outFile, 'utf8')
    const match = text.match(/BUILD_LABEL\s*=\s*'Build #(\d{8})\.(\d+)'/)
    if (!match) return null
    return { date: match[1], num: Number(match[2]) }
  } catch {
    return null
  }
}

async function main() {
  const today = getTodayStamp()
  const prev = await readPrevious()
  const next = prev && prev.date === today ? prev.num + 1 : 1
  const label = `Build #${today}.${next}`

  await mkdir(outDir, { recursive: true })
  await writeFile(
    outFile,
    `// Auto-generated by scripts/update-build-info.mjs\nexport const BUILD_LABEL = '${label}'\n`,
    'utf8',
  )

  console.log(label)
}

await main()
